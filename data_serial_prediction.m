function [m, e, phase1] = data_serial_prediction
%  extracted from Holland and Schiffino, 2016, Neurobiology of Learning and Memory

% shift sham
b = [
62.54480286738352, 616.2747909199522
60.876941457586625, 205.14695340501794];    
x{2}=[
836.6021505376344, 657.7060931899641
834.8482676224612, 266.5902031063321
834.8482676224612, 225.37395459976105];
% consistent sham
x{1}=[
970.7741935483871, 658.5830346475508
966.389486260454, 420.05495818399044
970.7741935483871, 393.7467144563919];
% shift lesioned
x{4}=[
794.7359617682199, 617.1087216248507
793.068100358423, 435.31182795698925
793.9020310633214, 410.2939068100359];
% consistent lesioned
x{3}=[
934.836320191159, 617.1087216248507
933.168458781362, 338.57586618876945
933.168458781362, 301.8829151732378];

base = (b(1,2)-b(2,2))*2; % multiply by 2 to get 100%
m = nan(1,4);
e = nan(1,4);
for i=1:4
    y = x{i};
    m(i) = y(1,2)-y(2,2);
    e(i) = y(2,2)-y(3,2);
end

m = m/base*100;
e = e/base*100;

% food cup response in phase 1 for the tone cue (Table 2; Holland and
% Gallagher 1993)
phase1 = [53.9 55.6 59.9 52.2];

end